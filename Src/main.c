/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used 
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void const * argument)
{

  /* USER CODE BEGIN 5 */
    FI_TIME_init();
    CONFIG_init();
  
    if (CONFIG_load(&config_) != RET_OK)
    {
        CONFIG_loadDefault(&config_);
        while(CONFIG_clear() != RET_OK)
        {
        }
        CONFIG_save(&config_);
        LOG_clear();
    }

    SYSTEM_init(&config_.system);
    LOG_init(&config_.log);
    SHELL_init(&config_.shell);
    ME_I10KL_init(&config_.me_i10kl);
    DEVICE_init(&config_.device);
  
    SHELL_start();
    ME_I10KL_start();

    TRACE_init(&config_.trace);
    CLIENT_init(&config_.client);

  /* Infinite loop */
    for(;;)
    {
//        HAL_IWDG_Refresh(&hiwdg);
        if (SYSTEM_getActivation())
        {
            CLIENT_loop();
            
            if (configSave_)
            {
                GlobalConfigSave();
                configSave_ = false;
            }
        }
        
        osDelay(1);
  }
  /* USER CODE END 5 */ 
}

